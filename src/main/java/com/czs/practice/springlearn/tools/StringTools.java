        if(null != ipList && ipList.length > 0){

            for(String ip : ipList){
                String[] ipArray = ip.split(":");
                returnValue.add(ipArray[0]);
            }

        }

        System.out.println("共有ip：" + returnValue.size() + " 个");
        for(String ipNow : returnValue){
            System.out.println(ipNow);
        }

        return returnValue;
